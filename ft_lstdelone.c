#include "includes/libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void *, size_t))
{
	del(alst[0]->content, alst[0]->content_size);
	alst[0]->next = NULL;
	free(alst[0]);
	alst[0] = NULL;
}
