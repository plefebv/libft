#include "includes/libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *lst;
	t_list *nextl;

	lst = *alst;
	while (lst)
	{
		nextl = lst->next;
		del(lst->content, lst->content_size);
		free(lst);
		lst = nextl;
	}
	*alst = NULL;
}
